// convertir un numero a texto (de 1 a 4)
let numero = 1;
if (numero == 1){
    console.log("UNO");
}
else if (numero == 2){
    console.log("DOS");
}
else if (numero == 3){
    console.log("TRES");
}
else if (numero == 1){
    console.log("CUANTRO");
}
else{
    console.log(`No se encuentra el numero ${numero} en la lista`);
}