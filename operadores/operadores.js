// operador suma
let a = 3, b = 2;
let z = a + b;
console.log(`El resultado de la suma es: ${z}`);

// operador resta 
z = a - b;
console.log(`El resultado de la resta es: ${z}`);

// operador multiplicacion
z = a * b;
console.log(`El resultado de la multiplicacion es: ${z}`);

// operador divicion
z = a / b;
console.log(`El resultado de la division es: ${z}`);

// operacion modulo
z = a % b;
console.log(`El resultado del modulo es: ${z}`);

// operador exponente
z = a ** b;
console.log(`El resultado del exponente de ${a} a la ${b} es: ${z}`);

// operador de incremento
// pre-incremento(el operador ++ antes de la variable)
z= ++a;
console.log(a);
console.log(z);

// post-incremento(el operador + despues de la variable)
z = b++;
console.log(b)
console.log(z)

// operador de decremento
// pre-decremento
z = --a;
console.log(a)
console.log(z)

// post-decremento
z = b--;
console.log(b)
console.log(z)

